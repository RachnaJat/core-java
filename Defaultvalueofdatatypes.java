public class Defaultvalueofdatatypes {

    static   byte a;
    static  short b;
    static  int c;
    static  long d;
    static  float e;
   static  double f;
   static   char g;
   static   boolean h;
    public static void main(String[] args)
     {
        
            //DEFAULT VALUE OF ALL PRIMITIVE DATA TYPES :-

        System.out.println("default value of byte= "+a);         //0
        System.out.println("default value of short= "+b);        //0
        System.out.println("default value of int= "+c);          //0
        System.out.println("default value of long= "+d);         //0
        System.out.println("default value of float= "+e);        //0.0
        System.out.println("default value of double= "+f);       //0.0
        System.out.println("default value of char= "+g);         // 
        System.out.println("default value of boolean= "+h);      // false
   
    }
    
}
